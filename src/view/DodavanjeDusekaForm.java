    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtNaziv.getText().isEmpty() || txtCena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti dusek");
            return;
        }
        String naziv = txtNaziv.getText();
        String cenaS = txtCena.getText();
        double cena;
        try {
            cena = Double.parseDouble(cenaS);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti dusek");
            return;
        }

        Materijal m = (Materijal) cbMaterijal.getSelectedItem();
        Tvrdoca t = (Tvrdoca) cbTvrdoca.getSelectedItem();
        int id = KontrolerK.getInstance().vratiIDDuseka();
        Dusek d = new Dusek(id, naziv, cena, m.getMaterijalID(), t.getTvrdocaID());

        boolean validiran = KontrolerK.getInstance().validirajDusekDodavanje(d);
        if (!validiran) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti dusek");
            return;
        }
        boolean signal = KontrolerK.getInstance().dodajDusek(d);
        if (signal) {
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio dusek");
            txtNaziv.setText("");
            txtCena.setText("");
            Velicina v = (Velicina) cbVelicine.getSelectedItem();
            KontrolerK.getInstance().sacuvajDimenzije(id, v);
        } else {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti dusek");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

