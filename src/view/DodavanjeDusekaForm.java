    private void srediFormu() {
        ArrayList<Materijal> listaMaterijala = KontrolerK.getInstance().vratiMaterijale();
        cbMaterijal.removeAllItems();
        for (Materijal materijal : listaMaterijala) {
            cbMaterijal.addItem(materijal);
        }
        ArrayList<Tvrdoca> listaTvrdoce = KontrolerK.getInstance().vratiTvrdoce();
        cbTvrdoca.removeAllItems();
        for (Tvrdoca tvrdoca : listaTvrdoce) {
            cbTvrdoca.addItem(tvrdoca);
        }
        ArrayList<Velicina> listaVelicina = KontrolerK.getInstance().vratiVelicine();
        cbVelicine.removeAllItems();
        for (Velicina v : listaVelicina) {
            cbVelicine.addItem(v);
        }
    }
